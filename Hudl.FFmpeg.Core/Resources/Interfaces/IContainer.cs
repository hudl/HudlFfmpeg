using System.Collections.Generic;

namespace Hudl.FFmpeg.Resources.Interfaces
{
    /// <summary>
    /// defines a base implmentation for an input/output file for ffmpege
    /// </summary>
    public interface IContainer
    {
        /// <summary>
        /// a .NET resource identifier used to define a unique Resource, multiple IResources may have the same ID
        /// </summary>
        string Id { get; set; }
        
        /// <summary>
        /// the file name of the resource that is used
        /// </summary>
        string Name { get; set; }

        /// <summary>
        /// the init file name of the resource that is used
        /// </summary>
        string InitName { get; set; }

        /// <summary>
        /// the file domain\directory for the resource
        /// </summary>
        string Path { get; set; }

        /// <summary>
        /// the init file domain\directory for the resource
        /// </summary>
        string InitPath { get; set; }

        /// <summary>
        /// a readable path for ffmpeg to access 
        /// </summary>
        string FullName { get; }

        /// <summary>
        /// a readable path of the init fragment for ffmpeg to access 
        /// </summary>
        string InitFullName { get; }

        /// <summary>
        /// the extension of the file, 
        /// </summary>
        string Format { get; }
        
        /// <summary>
        /// list of streams in the container
        /// </summary>
        List<IStream> Streams { get; set; }

        /// <summary>
        /// method for copying a resource for its base types.
        /// </summary>
        IContainer Copy();

        /// <summary>
        /// method for creating a new resource from the base resource with a new name.
        /// </summary>
        IContainer CreateFrom();
    }
}
